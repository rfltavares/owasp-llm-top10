class VulnerableLLMSystem:
    """Mock LLM system with various output handling vulnerabilities"""
    
    def __init__(self, security_level: str = "vulnerable"):
        self.security_level = security_level
        self.conversation_history = []
    
    def process_request(self, user_input: str, output_context: str = "html") -> Dict[str, Any]:
        """Process user request and return potentially vulnerable output"""
        
        # Simulate LLM generating response
        llm_response = self.generate_llm_response(user_input)
        
        # Apply output handling based on security level
        if self.security_level == "vulnerable":
            processed_output = self.vulnerable_output_handling(llm_response, output_context)
        elif self.security_level == "partially_secure":
            processed_output = self.partially_secure_handling(llm_response, output_context)
        else:  # secure
            processed_output = self.secure_output_handling(llm_response, output_context)
        
        return {
            'user_input': user_input,
            'llm_response': llm_response,
            'processed_output': processed_output,
            'context': output_context,
            'security_level': self.security_level
        }
    
    def generate_llm_response(self, user_input: str) -> str:
        """Generate mock LLM response that might contain dangerous content"""
        
        input_lower = user_input.lower()
        
        # Generate responses that might contain security issues
        if 'html' in input_lower or 'web' in input_lower:
            return f"Here's some HTML code: <script>alert('Generated by LLM')</script><p>Content: {user_input}</p>"
        
        elif 'sql' in input_lower or 'database' in input_lower:
            return f"Here's a SQL query: SELECT * FROM users WHERE name = '{user_input}' OR '1'='1'"
        
        elif 'command' in input_lower or 'script' in input_lower:
            return f"Here's a command: ls -la {user_input}; cat /etc/passwd"
        
        elif 'url' in input_lower or 'link' in input_lower:
            return f"Here's a URL: http://example.com/redirect?url={user_input}"
        
        elif 'file' in input_lower or 'path' in input_lower:
            return f"File path: ../../../etc/passwd or {user_input}"
        
        elif 'api' in input_lower or 'key' in input_lower:
            return f"API configuration: api_key = 'sk-1234567890abcdef' for {user_input}"
        
        else:
            # Include user input directly (potential injection point)
            return f"I understand you're asking about: {user_input}. Here's my response with your input included."
    
    def vulnerable_output_handling(self, content: str, context: str) -> str:
        """Vulnerable output handling - no sanitization"""
        
        if context == "html":
            # Direct HTML output - vulnerable to XSS
            return f"<div class='response'>{content}</div>"
        
        elif context == "sql":
            # Direct SQL construction - vulnerable to injection
            return f"SELECT * FROM responses WHERE content = '{content}'"
        
        elif context == "command":
            # Direct command construction - vulnerable to injection
            return f"echo '{content}' > output.txt"
        
        elif context == "url":
            # Direct URL construction - vulnerable to injection
            return f"https://api.example.com/search?q={content}"
        
        else:
            return content
    
    def partially_secure_handling(self, content: str, context: str) -> str:
        """Partially secure handling - some sanitization but incomplete"""
        
        if context == "html":
            # Basic HTML escaping but incomplete
            escaped = content.replace('<script>', '').replace('</script>', '')
            return f"<div class='response'>{escaped}</div>"
        
        elif context == "sql":
            # Basic SQL escaping but incomplete
            escaped = content.replace("'", "''")
            return f"SELECT * FROM responses WHERE content = '{escaped}'"
        
        elif context == "command":
            # Basic command escaping but incomplete
            escaped = content.replace(';', '').replace('|', '')
            return f"echo '{escaped}' > output.txt"
        
        else:
            return content
    
    def secure_output_handling(self, content: str, context: str) -> str:
        """Secure output handling - proper sanitization"""
        
        if context == "html":
            # Proper HTML encoding
            escaped = html.escape(content)
            return f"<div class='response'>{escaped}</div>"
        
        elif context == "sql":
            # Use parameterized query (simulated)
            return f"SELECT * FROM responses WHERE content = ? -- Parameter: {content}"
        
        elif context == "command":
            # Avoid shell execution, use safe alternatives
            safe_content = re.sub(r'[^a-zA-Z0-9\s\-_.]', '', content)
            return f"echo '{safe_content}' > output.txt"
        
        elif context == "url":
            # Proper URL encoding
            encoded = urllib.parse.quote(content)
            return f"https://api.example.com/search?q={encoded}"
        
        else:
            return content

