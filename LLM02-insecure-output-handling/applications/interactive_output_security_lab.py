    def get_context_for_scenario(self, scenario: SecurityScenario) -> str:
        """Get appropriate context for scenario testing"""
        
        context_map = {
            "XSS": "html",
            "SQL_INJECTION": "sql",
            "COMMAND_INJECTION": "command",
            "SSRF": "url",
            "PATH_TRAVERSAL": "file"
        }
        
        return context_map.get(scenario.vulnerability_type, "html")
    
    def analyze_output_security(self, result: Dict[str, Any]) -> List[str]:
        """Analyze output for security vulnerabilities"""
        
        vulnerabilities = []
        output = result['processed_output']
        
        # XSS detection
        if re.search(r'<script[^>]*>.*?</script>', output, re.IGNORECASE | re.DOTALL):
            vulnerabilities.append("XSS - Script Tag")
        
        if 'javascript:' in output.lower():
            vulnerabilities.append("XSS - JavaScript Protocol")
        
        if re.search(r'on\w+\s*=', output):
            vulnerabilities.append("XSS - Event Handler")
        
        # SQL Injection detection
        if re.search(r'(union\s+select|drop\s+table|delete\s+from)', output, re.IGNORECASE):
            vulnerabilities.append("SQL Injection")
        
        # Command Injection detection
        if re.search(r'(;\s*(rm|del|cat)|`[^`]*`|\$\([^)]*\))', output):
            vulnerabilities.append("Command Injection")
        
        # SSRF detection
        if re.search(r'(127\.0\.0\.1|localhost|169\.254\.169\.254)', output):
            vulnerabilities.append("SSRF - Internal Network")
        
        # Path Traversal detection
        if re.search(r'\.\.[\\/].*\.\.[\\/]', output):
            vulnerabilities.append("Path Traversal")
        
        # Information Disclosure
        if re.search(r'(api[_\s]?key|password)\s*[:=]\s*[^\s]+', output, re.IGNORECASE):
            vulnerabilities.append("Information Disclosure")
        
        return vulnerabilities
    
    def check_learning_objectives(self, scenario: SecurityScenario, vulnerabilities: List[str], test_count: int) -> bool:
        """Check if learning objectives are met"""
        
        # Basic completion criteria
        if test_count >= 3 and vulnerabilities:
            # Check if the right type of vulnerability was found
            vuln_type_map = {
                "XSS": ["XSS"],
                "SQL_INJECTION": ["SQL Injection"],
                "COMMAND_INJECTION": ["Command Injection"],
                "SSRF": ["SSRF"],
                "PATH_TRAVERSAL": ["Path Traversal"]
            }
            
            expected_vulns = vuln_type_map.get(scenario.vulnerability_type, [])
            found_expected = any(
                any(expected in vuln for expected in expected_vulns)
                for vuln in vulnerabilities
            )
            
            return found_expected
        
        return False
    
